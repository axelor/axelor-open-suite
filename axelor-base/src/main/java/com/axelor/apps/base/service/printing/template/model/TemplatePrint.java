package com.axelor.apps.base.service.printing.template.model;

import java.io.File;

public class TemplatePrint {

  protected String outputFormat;
  protected File print;

  public TemplatePrint() {}

  public TemplatePrint(String outputFormat, File print) {
    this.outputFormat = outputFormat;
    this.print = print;
  }

  public String getOutputFormat() {
    return outputFormat;
  }

  public void setOutputFormat(String outputFormat) {
    this.outputFormat = outputFormat;
  }

  public File getPrint() {
    return print;
  }

  public void setPrint(File print) {
    this.print = print;
  }
}
